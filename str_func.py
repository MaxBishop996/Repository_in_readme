def strupper(string: str):
    """
    функция делает буквы заглавными
    :param string: входная строка
    :return:
    """
    return string.upper()


def strcap(string: str):
    """
    функция делает первую букву заглавной
    :param string: входная строка
    :return:
    """
    return string.capitalize()
